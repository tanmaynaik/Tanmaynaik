# Break Statement
print("Break Statement")

for c in "break":
    if c == 'a':
        break
    print(c, end = '')


# Pass Statement
print("\n\n\nPass Statement")

if 2>1:
    pass

print("If block passed")


# Continue Statement
print("\n\n\nContinue Statement")

for t in "potato":
    if t == 't':
        continue
    print(t,end = '')